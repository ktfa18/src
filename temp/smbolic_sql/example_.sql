SELECT
	CASE
		WHEN T1.C1 + 1 = 0 THEN 0
		ELSE 1
	END AS F1,
	CASE
		WHEN T1.C2 IS NULL OR 
			   T2.C2 IS NULL THEN NULL
		ELSE T1.C2 + T2.C2
	END AS F2
FROM
	T1 INNER JOIN T2 ON T1.C1 = T2.C1
WHERE
	T2.C2 > 0;

CREATE TABLE T1(
	C1 NUMBER(2) NOT NULL CHECK(T1.C1 < 1),
	C2 NUMBER(1) NULL
);

CREATE TABLE T2(
	C1 NUMBER(2) NOT NULL,
	C2 NUMBER(1) NULL
);
