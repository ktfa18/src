SELECT
	CASE
		WHEN S.S1 = 1 THEN 
			CASE
				WHEN L.L1 = 2 THEN 111
				ELSE 222
			END
		WHEN S.S1 = 2 THEN 333
		ELSE 444
	END AS COL1
FROM
	S,L
WHERE
	S.S1 > 0;

CREATE TABLE S(
	S1 NUMBER(1) NOT NULL,
	S2 NUMBER(1) NOT NULL,
	S3 NUMBER(1) NOT NULL
);

CREATE TABLE L(
	L1 NUMBER(1) NOT NULL CHECK(L.L1>0)
);

